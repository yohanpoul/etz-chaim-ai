#!/usr/bin/env python3
"""dump_sifrei_yesod.py — generate idempotent SQL dump of sifrei_yesod tables.

Connects to the dev DB (`ETZ_CHAIM_DB_URL` or default), reads every row of the
8 `sifrei_yesod_*` tables in FK-safe order, and writes
`INSERT … ON CONFLICT DO UPDATE/NOTHING` statements to
`etzchaim/deploy/init-db/99-sifrei_yesod-data.sql`.

The output is consumed by `etzchaim update` via `_apply_migrations()` (cf.
etzchaim/cli/commands/update.py), so users automatically receive every newly
transposed perek the next time they run `etzchaim update`.

The ON CONFLICT clauses mirror sofer.py (`ensure_*` / `ingest_perek`) so the
dump is semantically identical to a fresh re-run of sofer.

FK to perakim.id is resolved at apply time via a sub-SELECT on
(sefer_id, heikhal_number, shaar_number, perek_number), so the SERIAL ids of
the dev DB do not leak into users' DBs.

Usage:
    python scripts/dump_sifrei_yesod.py
    python scripts/dump_sifrei_yesod.py --output /path/to/file.sql
    python scripts/dump_sifrei_yesod.py --db postgresql://user@host/db
"""
from __future__ import annotations

import argparse
import datetime
import os
import sys
from pathlib import Path

import psycopg2
import psycopg2.extras

DB_URL = (
    os.environ.get("ETZ_CHAIM_DB_URL")
    or os.environ.get("ETZ_CHAIM_DB", "postgresql://localhost/etz_chaim")
)
REPO_ROOT = Path(__file__).resolve().parent.parent
DEFAULT_OUTPUT = REPO_ROOT / "etzchaim" / "deploy" / "init-db" / "99-sifrei_yesod-data.sql"


def _mog(cur, sql: str, params: tuple) -> str:
    """Build a single INSERT statement string from a parameterized template."""
    return cur.mogrify(sql, params).decode("utf-8") + ";\n"


def _header(counts: dict[str, int]) -> str:
    now = datetime.datetime.now(datetime.timezone.utc).isoformat(timespec="seconds")
    summary = ", ".join(f"{v} {k}" for k, v in counts.items() if v > 0)
    return (
        "-- 99-sifrei_yesod-data.sql\n"
        "-- AUTO-GENERATED by scripts/dump_sifrei_yesod.py — DO NOT EDIT MANUALLY.\n"
        "-- Re-runs idempotently via ON CONFLICT DO UPDATE / DO NOTHING.\n"
        "-- Applied by etzchaim/cli/commands/update.py:_apply_migrations() so every\n"
        "-- `etzchaim update` ships the latest Sifrei Yesod corpus.\n"
        f"-- Generated at: {now}\n"
        f"-- Contents: {summary}\n"
        "\n"
        "BEGIN;\n"
    )


# ── Section emitters ─────────────────────────────────────────────


def _dump_sefarim(cur, out: list[str]) -> int:
    cur.execute("SELECT * FROM sifrei_yesod_sefarim ORDER BY sefer_id")
    rows = cur.fetchall()
    out.append(f"\n-- ═══ sefarim ({len(rows)} rows) ═══\n")
    sql = """INSERT INTO sifrei_yesod_sefarim
        (sefer_id, titre_he, titre_fr, auteur, maitre, edition_base,
         date_composition, structure_type, nombre_shaarim, description)
    VALUES (%s, %s, %s, %s, %s, %s, %s, %s, %s, %s)
    ON CONFLICT (sefer_id) DO UPDATE SET
        titre_he = EXCLUDED.titre_he,
        titre_fr = EXCLUDED.titre_fr,
        auteur = EXCLUDED.auteur,
        maitre = EXCLUDED.maitre,
        edition_base = EXCLUDED.edition_base,
        date_composition = EXCLUDED.date_composition,
        structure_type = EXCLUDED.structure_type,
        nombre_shaarim = EXCLUDED.nombre_shaarim,
        description = EXCLUDED.description,
        updated_at = NOW()"""
    for row in rows:
        out.append(_mog(cur, sql, (
            row["sefer_id"], row["titre_he"], row["titre_fr"], row["auteur"],
            row["maitre"], row["edition_base"], row["date_composition"],
            row["structure_type"], row["nombre_shaarim"], row["description"],
        )))
    return len(rows)


