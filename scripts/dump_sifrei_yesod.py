def _dump_perakim(cur, out: list[str]) -> tuple[int, dict[int, tuple]]:
    """Dump perakim and return mapping {dev_id: (sefer, heikhal, shaar, perek)}.

    The mapping is needed to translate `perek_id` foreign keys in the assertions/
    relations/principes inserts into a sub-SELECT against the user's DB at apply
    time (since SERIAL ids differ between dev and prod DBs).
    """
    cur.execute(
        "SELECT * FROM sifrei_yesod_perakim "
        "ORDER BY sefer_id, heikhal_number, shaar_number, perek_number"
    )
    rows = cur.fetchall()
    out.append(f"\n-- ═══ perakim ({len(rows)} rows) ═══\n")
    sql = """INSERT INTO sifrei_yesod_perakim
        (sefer_id, heikhal_number, shaar_number, perek_number,
         source_edition, transposed_by, version, strates, yaml_hash)
    VALUES (%s, %s, %s, %s, %s, %s, %s, %s, %s)
    ON CONFLICT (sefer_id, heikhal_number, shaar_number, perek_number) DO UPDATE SET
        source_edition = EXCLUDED.source_edition,
        transposed_by = EXCLUDED.transposed_by,
        version = EXCLUDED.version,
        strates = EXCLUDED.strates,
        yaml_hash = EXCLUDED.yaml_hash,
        updated_at = NOW()"""
    perek_id_to_coords: dict[int, tuple] = {}
    for row in rows:
        perek_id_to_coords[row["id"]] = (
            row["sefer_id"], row["heikhal_number"],
            row["shaar_number"], row["perek_number"],
        )
        out.append(_mog(cur, sql, (
            row["sefer_id"], row["heikhal_number"], row["shaar_number"],
            row["perek_number"], row["source_edition"], row["transposed_by"],
            row["version"], row["strates"], row["yaml_hash"],
        )))
    return len(rows), perek_id_to_coords


def _dump_concepts(cur, out: list[str]) -> int:
    """Concepts: ON CONFLICT DO NOTHING (matches sofer.py:295-302)."""
    cur.execute("SELECT * FROM sifrei_yesod_concepts ORDER BY id")
    rows = cur.fetchall()
    out.append(f"\n-- ═══ concepts ({len(rows)} rows) ═══\n")
    sql = """INSERT INTO sifrei_yesod_concepts
        (concept_id, nom_he, nom_fr, description, domaine,
         premiere_apparition, metadata)
    VALUES (%s, %s, %s, %s, %s, %s, %s)
    ON CONFLICT (concept_id) DO NOTHING"""
    for row in rows:
        # metadata is JSONB → wrap in psycopg2.extras.Json so mogrify emits
        # a properly-quoted JSON literal.
        meta = psycopg2.extras.Json(row["metadata"]) if row["metadata"] is not None else None
        out.append(_mog(cur, sql, (
            row["concept_id"], row["nom_he"], row["nom_fr"], row["description"],
            row["domaine"], row["premiere_apparition"], meta,
        )))
    return len(rows)


# Sub-SELECT template used to resolve perek_id from logical coords.
# Returns NULL if the perek is missing — but FK NOT NULL on the table will
# raise, which is the desired behavior (perakim must be inserted first).
_PEREK_LOOKUP = (
    "(SELECT id FROM sifrei_yesod_perakim "
    "WHERE sefer_id = %s AND heikhal_number = %s "
    "AND shaar_number = %s AND perek_number = %s)"
)


def _dump_assertions(cur, out: list[str], coords_map: dict[int, tuple]) -> int:
    cur.execute("SELECT * FROM sifrei_yesod_assertions ORDER BY id")
    rows = cur.fetchall()
    out.append(f"\n-- ═══ assertions ({len(rows)} rows) ═══\n")
    sql = f"""INSERT INTO sifrei_yesod_assertions
        (assertion_id, perek_id, source_he, source_ref, assertion,
         assertion_type, concepts, mapping_modules, mapping_tables,
         mapping_partzufim, mapping_relevance, strate)
    VALUES (%s, {_PEREK_LOOKUP}, %s, %s, %s, %s, %s, %s, %s, %s, %s, %s)
    ON CONFLICT (assertion_id) DO UPDATE SET
        perek_id = EXCLUDED.perek_id,
        source_he = EXCLUDED.source_he,
        source_ref = EXCLUDED.source_ref,
        assertion = EXCLUDED.assertion,
        assertion_type = EXCLUDED.assertion_type,
        concepts = EXCLUDED.concepts,
        mapping_modules = EXCLUDED.mapping_modules,
        mapping_tables = EXCLUDED.mapping_tables,
        mapping_partzufim = EXCLUDED.mapping_partzufim,
        mapping_relevance = EXCLUDED.mapping_relevance,
        strate = EXCLUDED.strate,
        embedding = NULL,
        updated_at = NOW()"""
    for row in rows:
        coords = coords_map[row["perek_id"]]
        concepts = psycopg2.extras.Json(row["concepts"]) if row["concepts"] is not None else None
        out.append(_mog(cur, sql, (
            row["assertion_id"],
            *coords,
            row["source_he"], row["source_ref"], row["assertion"],
            row["assertion_type"], concepts,
            row["mapping_modules"], row["mapping_tables"],
            row["mapping_partzufim"], row["mapping_relevance"],
            row["strate"],
        )))
    return len(rows)


