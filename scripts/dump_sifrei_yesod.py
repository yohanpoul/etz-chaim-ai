def _dump_cross_refs(cur, out: list[str]) -> int:
    """cross_refs has no UNIQUE constraint other than `id` (SERIAL).

    To keep the dump idempotent we WHERE NOT EXISTS the full natural key
    (source_assertion_id, target_assertion_id, target_sefer, target_ref,
    relation_type) — including target_assertion_id so duplicate auto-generated
    refs that point to different target_assertion_ids are preserved.
    target_assertion_id may be NULL, so we use IS NOT DISTINCT FROM.
    """
    cur.execute("SELECT * FROM sifrei_yesod_cross_refs ORDER BY id")
    rows = cur.fetchall()
    if not rows:
        return 0
    out.append(f"\n-- ═══ cross_refs ({len(rows)} rows) ═══\n")
    sql = """INSERT INTO sifrei_yesod_cross_refs
        (source_assertion_id, target_assertion_id, target_sefer,
         target_ref, relation_type, description)
    SELECT %s, %s, %s, %s, %s, %s
    WHERE NOT EXISTS (
        SELECT 1 FROM sifrei_yesod_cross_refs
        WHERE source_assertion_id = %s
          AND target_assertion_id IS NOT DISTINCT FROM %s
          AND target_sefer = %s
          AND target_ref = %s
          AND relation_type = %s
    )"""
    for row in rows:
        out.append(_mog(cur, sql, (
            row["source_assertion_id"], row["target_assertion_id"],
            row["target_sefer"], row["target_ref"],
            row["relation_type"], row["description"],
            # WHERE NOT EXISTS params
            row["source_assertion_id"], row["target_assertion_id"],
            row["target_sefer"], row["target_ref"], row["relation_type"],
        )))
    return len(rows)


# ── Entry point ──────────────────────────────────────────────────


def dump(db_url: str, output_path: Path) -> dict[str, int]:
    """Generate the SQL dump file. Returns row counts per table."""
    conn = psycopg2.connect(db_url)
    cur = conn.cursor(cursor_factory=psycopg2.extras.RealDictCursor)

    out: list[str] = []
    out.append("__HEADER__\n")  # placeholder, replaced after we know counts

    counts: dict[str, int] = {}
    counts["sefarim"] = _dump_sefarim(cur, out)
    counts["heikhalot"] = _dump_heikhalot(cur, out)
    counts["shaarim"] = _dump_shaarim(cur, out)
    counts["perakim"], coords_map = _dump_perakim(cur, out)
    counts["concepts"] = _dump_concepts(cur, out)
    counts["assertions"] = _dump_assertions(cur, out, coords_map)
    counts["relations"] = _dump_relations(cur, out, coords_map)
    counts["principes"] = _dump_principes(cur, out, coords_map)
    counts["cross_refs"] = _dump_cross_refs(cur, out)

    out.append("\nCOMMIT;\n")
    out[0] = _header(counts)

    conn.close()

    output_path.parent.mkdir(parents=True, exist_ok=True)
    output_path.write_text("".join(out), encoding="utf-8")
    return counts


def main() -> None:
    parser = argparse.ArgumentParser(
        prog="dump_sifrei_yesod",
        description="Dump sifrei_yesod_* tables to an idempotent SQL file.",
    )
    parser.add_argument("--db", default=DB_URL, help="PostgreSQL URL")
    parser.add_argument(
        "--output",
        type=Path,
        default=DEFAULT_OUTPUT,
        help=f"Output SQL path (default: {DEFAULT_OUTPUT})",
    )
    args = parser.parse_args()

    print(f"Reading from: {args.db}")
    print(f"Writing to:   {args.output}")
    try:
        counts = dump(args.db, args.output)
    except psycopg2.Error as exc:
        print(f"  ✗ PostgreSQL error: {exc}", file=sys.stderr)
        sys.exit(1)

    size_kb = args.output.stat().st_size / 1024
    print(f"\n  ✓ {size_kb:.1f} KB written")
    for table, n in counts.items():
        print(f"    {table:14s} {n:>6d}")


if __name__ == "__main__":
    main()
